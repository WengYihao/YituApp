public class LatlngBean {
    private double lat;
    private double lng;
    private int order;

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }

    public int getOrder() {
        return order;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public void setOrder(int order) {
        this.order = order;
    }
}
